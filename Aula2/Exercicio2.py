# Exercicio 1
user_number = int(input('Digite seu número: '))
sub_number = int(user_number - 1)
adc_number = int(user_number + 1)
plus_number = int(user_number * 2)
raiz_number = int(user_number ** (1/2))

print('O antecessor do número', user_number, 'é', sub_number, '.')
print('O sucessor do número', user_number, 'é', adc_number, '.')
print('O dobro do número', user_number, 'é', plus_number, '.')
print('A raiz quadrada do número', user_number, 'é', raiz_number, '.')
